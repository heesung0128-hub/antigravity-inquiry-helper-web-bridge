// 학년과 현재 연도를 기준으로 교육과정 자동 결정
function getDefaultCurriculum(year, grade) {
  if (year >= 2028) return "v2022";
  if (year === 2027) return "v2022";
  if (year === 2026) return grade === 3 ? "v2015" : "v2022";
  if (year === 2025) return grade === 1 ? "v2022" : "v2015";
  return "v2015";
}

// 보통 특정 학년에서 배우는 교과목 추천 및 분류 확인용 도구
function validateGradeAndSubject(grade, subjectCategory) {
  // 수학/과학 공통과목은 대개 1학년용
  if (grade !== 1 && subjectCategory === "공통") {
    return "공통 과목은 주로 1학년 과정입니다. 학년과 교과목이 맞는지 확인해 보세요.";
  }
  // 일반선택/진로선택/융합선택 과목은 주로 2,3학년용
  if (grade === 1 && (subjectCategory === "일반선택" || subjectCategory === "진로선택" || subjectCategory === "융합선택")) {
    return "선택 과목(일반/진로/융합)은 대개 2~3학년 과정입니다. 확인해 주세요.";
  }
  return null;
}
